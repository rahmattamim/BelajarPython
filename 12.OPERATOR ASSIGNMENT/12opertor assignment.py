# operasi yang dapat di singkat

# aritmatika
a = 5
print ("nilai a =",a)

a += 1 #sama kaya a = a + 1
print ("nilai a += 1 nilai a menjadi",a)

a -= 2 # sama kaya a = a - 2
print ("nilai a -= 2 nilai a menjadi",a)

a *= 5 # sama kaya a = a * 5
print ("nilai a *= 5 nilai a menjadi",a)

a /= 2 # sama kaya a = a / 2
print ("nilai a /= 2 nilai a menjadi",a)

# modulus dan floor divison
b = 10
print ("nilai b =",b)

b %= 3 #modulus
print ("nilai b %= 3 nilai b menjadi",b)

print("\n")
b = 10
print ("nilai b =",b)

b //= 3 #dibulatkan
print ("nilai b //= 3 nilai b menjadi",b)

a = 5
print ("nilai a =",a)
# pangkat
a **= 3 #sama kaya a = a ** 3 (pangkat)
print ("nilai a **= 3 nilai a menjadi",a)

# operasi bitwise

# or
c = True
print("\nnilai c =",c)
c |= False
print ("nilai c |= false nilai c menjadi",c)
c = False
print("\nnilai c =",c)
c |= False
print ("nilai c |= false nilai c menjadi",c)

# and
c = True
print("\nnilai c =",c)
c &= False
print ("nilai c &= false nilai c menjadi",c)
c = True
print("\nnilai c =",c)
c &= True
print ("nilai c &= false nilai c menjadi",c)

# xor
c = True
print("\nnilai c =",c)
c ^= False
print ("nilai c ^= false nilai c menjadi",c)
c = True
print("\nnilai c =",c)
c ^= True
print ("nilai c ^= false nilai c menjadi",c)

# yang geser
d = 0b0100
print("\nnilai d =",format(d,'05b'))
d >>= 2
print ("nilai d >>= false nilai d menjadi",format(d,'05b'))
d <<= 1
print ("nilai d <<= false nilai d menjadi",format(d,'05b'))

